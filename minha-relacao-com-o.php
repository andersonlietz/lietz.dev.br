        <h1>Minha relação com o PHP</h1>

        <p>Lembro que eu queria saber como alguém constrói um site ou aplicativo e faz com que eles apareçam nos celulares e computadores pelo mundo. Tinha essa curiosidade, era algo misterioso, quase místico, mas não conseguia me imaginar fazendo parte disso, acreditava não ter as habilidades de raciocínio lógico e muito menos com as “máquinas”. No facebook alguns memes sobre o PHP começaram a chegar, recomendações de alguns grupos de tecnologia nos quais alguns conhecidos provavelmente participavam.</p>

<p>Um amigo que trabalha com PHP passou a recomendar que eu estudasse programação, e frequentemente me mandava o link de uma graduação em ADS. Eu perdia a graça, pois tinha vontade e sabia que era o ideal, mas prestava muita atenção nos obstáculos. Por fim aceitei, fiz o cadastro, passei no vestibular e logo estava devidamente matriculado como aluno da graduação em Tecnologia em Análise e Desenvolvimento de Sistemas.</p>


<p>Meu amigo, é claro, ficou muito feliz, e recomendou que eu fosse em busca de um estágio e fizesse um site em PHP para demonstrar meus conhecimentos na área aos recrutadores e futuros patrões. Baixei o VSCode, o Xampp, e comecei a assistir uma playlist no youtube sobre como fazer um site, e me encantei com o meu primeiro “Hello World”.</p>


<p>Hoje fico até constrangido em lembrar os erros tão crassos que cometi criando as minhas primeiras páginas web. Erros como esquecer de fechar uma tag, redundâncias no arquivo CSS, etc. Depois me acostumei com o básico dessas tecnologias e cometi o erro de ficar estagnado, por ter um certo “medo” das linguagens de programação propriamente ditas.</p>


<p>Os incontáveis erros nos arquivos PHP e JavaScript também me frustravam, e me impediam de seguir adiante em minhas buscas pelo domínio das linguagens. Mas continuava “satisfeito” com as páginas  web feitas com HTML e CSS, o que me impedia de desistir de vez. Com o tempo construí um site onde posto algumas crônicas (vacilouviroucronica.com) e este portfólio.</p>


<p>Com o desejo de aprender a construir sites realmente em PHP, criei um sistema de login e comentários com controle de usuários baseado em um tutorial. Novamente, vários erros, mas menos do que anteriormente. O sistema de login funcionou depois de muito estudo sobre as sessões em PHP, mas o sistema de comentários precisa de algumas correções.</p>


<p>Segui mais alguns tutoriais, como o de MVC e URL amigável, e decidi que estava na hora de  focar mais no aprendizado do que  na execução, comprei um curso completo de PHP. Apesar de me frustrar de vez em quando por aprender e fazer os exercícios de coisas muito básicas, percebo que realmente é isso que eu precisava, saber a teoria que está por trás do código. </p>


<p>Um tempo depois que eu comecei esse curso, a matéria de Linguagens de Servidor da faculdade usou PHP como exemplo, e me dediquei a estudar aquelas aulas que eu desejava ter desde que comecei a faculdade, mas que tinham sido adiadas porque as outras matérias foram abordadas em Java. </p>


<p>Apesar desses estudos, entendo que trabalhando na área aprenderei muito  mais, então sigo na minha busca por um lugar ao sol. Enquanto isso vou estudando, errando e corrigindo, comemorando conquistas e aprendizados, porque o que eu sei hoje é muito mais do que eu sabia quando comecei.</p>

